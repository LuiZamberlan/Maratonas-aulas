//Funções
function soma(numero) {
    return numero + 11
}
//Função criada de forma comum, nela temos o return que tras um valor e pode ser imprimido ou utilizado de várias formas

console.log(soma(11))

let soma1 = (numero) => {
    return numero + 1
}
//Função criada dentro de uma variável.

console.log(soma1(11))
